// For loop example
console.log("For Loop Example:");
for (let i = 0; i < 5; i++) {
    console.log("Iteration", i);
}

// While loop example
console.log("\nWhile Loop Example:");
let j = 0;
while (j < 5) {
    console.log("Iteration", j);
    j++;
}

// Do-While loop example
console.log("\nDo-While Loop Example:");
let k = 0;
do {
    console.log("Iteration", k);
    k++;
} while (k < 5);
