// Variable declaration and assignment
let age = 23;
let name = "Nirdesh Ji";
const PI = 3.14; // Constant variable

// Printing variables
console.log("Name:", name);
console.log("Age:", age);
console.log("PI:", PI);

// Variable reassignment
age = 24;
name = "Shailendra";

// Printing variables after reassignment
console.log("\nAfter reassignment:");
console.log("Name:", name);
console.log("Age:", age);

// Using variables in calculations
let radius = 5;
let area = PI * radius * radius;
console.log("\nArea of a circle with radius", radius, "is", area);
